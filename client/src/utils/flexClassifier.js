//"EF_Flex", "IF_Flex", "MF_Flex", "PF_Flex", "RF_Flex", "TF_Flex", "WF_Flex"
export const classifyFlexValue01 = (value) => {
  if (value < 5000) return "No Bend";
  if (value < 3200) return "Almost No Bend";
  if (value < 1400) return "Almost Full Bend";
  return "Full Bend";
};
